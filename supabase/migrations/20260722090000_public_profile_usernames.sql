-- Public profile usernames
--
-- Guarantees:
--   * every claimed/authenticated profile receives a stable public username;
--   * generated fallbacks never expose or encode the user's email address;
--   * usernames are canonical lowercase URL slugs and unique case-insensitively;
--   * reserved application routes cannot be claimed;
--   * a user can change only their own username through a narrow RPC.

create or replace function public.normalize_profile_username(p_value text)
returns text
language sql
immutable
strict
set search_path = public
as $$
  select trim(both '-' from regexp_replace(
    regexp_replace(
      lower(translate(
        p_value,
        'àáâãäåæçèéêëìíîïñòóôõöøœùúûüýÿšž',
        'aaaaaaaceeeeiiiinooooooouuuuyysz'
      )),
      '[^a-z0-9]+', '-', 'g'
    ),
    '-{2,}', '-', 'g'
  ));
$$;

comment on function public.normalize_profile_username(text) is
  'Canonicalizes a proposed public-profile username to a lowercase URL slug.';

